# Arithmetic operators -> Arithmetic Operators are used to perform basic mathematical operations such as addition, subtraction, multiplication, division, and modulus.


# ** -> Exponent (raises a number to the power of another)
num1=20
num2=5
print("Addition :",num1+num2)
print("Substraction :",num1-num2)
print("Multiplcation:",num1*num2)
print("division :",num1/num2)
print("Floor division :",num1//num2)   # // -> Floor division (returns the integer quotient)
print("Modulus:",num1%num2)            # %  -> Modulus (returns the remainder)
print("Exponent :",num1**num2)         # ** -> Exponent (raises a number to the power of another)