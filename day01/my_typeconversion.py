# Type conversion -> Type conversion is the process of converting one data type into another.
# int() → Converts to an integer 
num = int("30")

print(num)
print(type(num))



# float() → Converts to a float 

price = float("22.6")

print(price)
print(type(price))

# str() → Converts to a string

age = str(20)

print(age)
print(type(age))

#  bool() → Converts to a boolean

value = bool(1)

print(value)
print(type(value))