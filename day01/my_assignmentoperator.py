# Assignment variables -> Assignment operators are used to assign values to variables.
num=8     #same as num=8
print("Assign:",num)

num += 3  #same as num=num+3
print("Add and assign:",num )

num-=3   #same as num=num-3
print("Subtract and assign:",num)


num*=3   #same as num=num*3
print("Multiply and assign:",num)


num/=3   #same as num=num/3
print("Divide and assign:",num)


num%=3   #same as num=num%3
print("Modulus and assign:",num)


num//=3  #same as num=num//3
print("Floordivide and assign:",num)


num**=3  #same as num=num**3
print("Exponent and assign:",num)
