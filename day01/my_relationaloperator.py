#Relational Operators -> Relational operators are used to compare two values.

num1=20
num2=5

print("Equal to :",num1==num2) # True if num1 is equal to num2
print("Not Equal to :",num1!=num2) # True if num1 is not equal to num2
print("Greator than :",num1>num2) #True if num1 is greator than num2 
print("Less than :",num1<num2) #True if num1 is less than num2 
print("Greator than or equal to:",num1>=num2) #True if num1 is greator than or equal to num2 
print("Less than or equal to:",num1<=num2) #True if num1 is less than or equal to num2 


