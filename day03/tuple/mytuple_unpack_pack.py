# Tuple Packing: Combining multiple values into a single tuple
# Tuple Unpacking: Assigning tuple values to multiple variables

person=("anna",25,"teacher")
print("\nTuple packing")
print("*" * 30)

print("tuple packing:",person)


print("\nTuple Unpacking")
print("*" * 30)

name,age,role=person
print("Name:",name)
print("Age:",age)
print("Role:",role)

