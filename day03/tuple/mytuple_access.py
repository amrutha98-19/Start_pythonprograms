
 # Access tuple elements using positive and negative indexing.
flowers=('rose','jasmin','lotus','lily')

# Positive indexing → Start from the beginning
# Counts from left to right.
# Starts with 0.

print("First element:",flowers[0]) # First element
print("Third element:",flowers[2]) # Third element

# Negative indexing → Start from the end
# Counts from right to left.
# Starts with -1.
print("Last element:",flowers[-1]) # Last element
print("Second-Last element:",flowers[-2]) # Second last element
