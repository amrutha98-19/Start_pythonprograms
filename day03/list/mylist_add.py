# Add Elements
# append() -> Adds one item to the end of the list
# insert() -> Adds one item at a specific index
# extend() -> Adds multiple items to the end of the list
mylist=[5,6,7,8,9] 
print("List:",mylist)

#append()

mylist.append(10)
print("Append:",mylist)

# insert()

mylist.insert(1,3)
print("Insert:",mylist)

#extend()
mylist.extend([11,12,13])
print("Extend:",mylist)